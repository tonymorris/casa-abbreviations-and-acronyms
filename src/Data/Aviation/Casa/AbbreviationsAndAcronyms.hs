data Source =
  AIP
  | AIS
  | ATA
  | ATSB
  | CAR1988
  | CAR1998
  | CASA
  | CASR1998
  | CASRPart String
  | FAR
  | ICAO (Maybe String)
  | JAA
  | OBPR
  | ORR
  | WATOG
  | OtherSource String
  deriving (Eq, Ord, Show)

makeClassy ''Source

newtype Sources =
  Sources
    [Source]
  deriving (Eq, Ord, Show)

makeWrapped ''Sources

data Acronym =
  Acronym {
    _name ::
      String
  , _meaning :: 
      String
  , _sources ::
      Sources
  } deriving (Eq, Ord, Show)  

makeClassy ''Acronym

newtype Acronyms =
  Acronyms
    [Acronym]
  deriving (Eq, Ord, Show)

makeWrapped ''Acronyms

